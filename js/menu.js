function openNav() {
  document.getElementById("mySidenav").style.width = "30%";
  document.getElementById("div_body").style.marginLeft = "30%";
}

/* Set the width of the side navigation to 0 and the left margin of the page content to 0,cambiar lugo el div_body por el ide correspondiente al cuerpo */
function closeNav() {
  document.getElementById("mySidenav").style.width = "0";
  document.getElementById("div_body").style.marginLeft = "0";
}